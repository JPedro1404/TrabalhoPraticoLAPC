#Criação do laço for para enumerar cada caractere da frase 'Olá, laço for.'
texto = 'Olá, laço for.'
for item in texto:
    print('Caractere: ' , item)
#Criação do laço for para realizar a iteração sobre o intervalo númerico entre 1 e 10
for i in range (1, 11):
    print('Número do intervalo: ' , str(i))

